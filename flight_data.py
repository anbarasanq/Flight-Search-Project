class FlightData:
    def __init__(self, price, origin_city,
                 origin_airport, destination_city, destination_airport, out_date, return_date):
        self.money = price
        self.cityfrom  = origin_city
        self.flyfrom = origin_airport
        self.cityto = destination_city
        self.flyto = destination_airport
        self.out_date = out_date
        self. return_date = return_date






